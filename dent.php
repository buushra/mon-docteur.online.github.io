<h1 class="h1">Dents de lait : pourquoi faut-il en prendre soin ?</h1>
<p class="p1">Même si elles finiront par tomber (entre 6 et 13 ans selon les dents) et par être remplacée par des dents définitives, les dents de lait sont indispensables chez les enfants. En effet, ce sont elles qui vont définir le visage du tout-petit, mais surtout elles vont guider les dents définitives pour qu'elles poussent droites et dans la bonne position. Enfin, elles sont essentielles pour manger et pour le développement du langage.</p>

