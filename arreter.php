<h1 class="h1">Arrêter brutalement ou progressivement ?</h1>

<p class="p1">Alors qu'on pensait hier que seul l'arrêt total du tabac menait à la réussite, on sait aujourd'hui qu'un sevrage progressif est possible. Et là encore, en fonction de chacun, on pourra choisir la méthode la plus adaptée. Et parce que le tabagisme est un comportement renforcé par une dépendance, dont la nicotine est principalement responsable, seule une minorité de fumeurs parvient à une abstinence permanente dès la première tentative d’arrêt, alors que la majorité persiste dans une consommation de tabac sur plusieurs années selon une alternance de périodes de rechutes et d’abstinence.<br>
Pour le Dr Peim, "c'est souvent très anxiogène pour un fumeur d'arrêter brutalement et ce stress peut être synonyme d'échec. Il peut être pertinent de supprimer d'abord les cigarettes de la journée. Tous les fumeurs qui travaillent savent combien descendre les 25 étages de son bureau 10 fois par jour pour fumer peut être contraignant… L'usage du substitut dans la journée permettra de se passer de ces cigarettes et d'aller progressivement vers un arrêt total".<br>
Quelle que soit la méthode choisie, la personne qui décide d'arrêter de fumer doit devenir l'acteur principal de son sevrage. Le bon timing et la bonne méthode lui appartiennent. 
</p>

<h1 class="h2">Arrêter seul ou se faire aider ?</h1>

<p class="p1">Si vous décidez d’arrêter de fumer, vous pouvez choisir de le faire seul ou bien d’être entouré par des professionnels de santé.<br>

Les fumeurs qui entreprennent leur démarche seul peuvent bénéficier :<br>
 
    - D’un accompagnement téléphonique grâce à Tabac Info Service ; 
    - D'un coaching personnalisé en téléchargeant l’application Tabac Info Service ;
    - La ligne 3989 et le site internet permettent également aux internautes d’accéder à un suivi personnalisé dans leur démarche d’arrêt ou celle de leurs  proches, par messagerie.
 Pourtant, les chiffres ne mentent pas : plus de neuf fois sur dix, les fumeurs n’arrivent pas à arrêter leur consommation de tabac sans se faire aider par des professionnels de santé. Dans ce cas, il est conseillé de faire appel à des spécialistes qui pourront vous conseiller et vous orienter, tels que des tabacologues, des addictologues ou encore des psychologues.<br>
 L’implication des médecins traitants, grâce à leur rôle de prévention et leur proximité avec les patients, est indispensable. Cela suppose pour certains d’entre eux une sensibilisation préalable. Le rôle d’autres professionnels de santé (sages-femmes et infirmiers, notamment) est également non-négligeable.<br>
 Selon les recommandations de l’Agence nationale de sécurité des médicaments (ANSM), l’aide au sevrage tabagique peut prendre des formes variées et plus ou moins intensives selon l’importance des dépendances et des difficultés à l’arrêt, allant du conseil minimal donné par tout professionnel de santé à l’intervention de tabacologues pour les formes les plus sévères.<br>
</p>

