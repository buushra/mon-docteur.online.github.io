        <h2>Une langue chargée :</h2>
        <p>Les mauvaises odeurs peuvent aussi émaner des bactéries situées sur la langue. Le dessus de la langue, mal irrigué par la salive peut effectivement entrainer une putréfaction des bactéries.</p>
        <h2>Quel traitement ? </h2>
        <p>Réaliser quelques bains de bouche que vous pouvez effectuer quotidiennement pour rafraichir la bouche à base de menthol ou de fluor. Il existe d’autres produits dont les composants (fluorure d’étain notamment) inactivent les bactéries et ralentissent le métabolisme bactérien et empêchent ainsi la formation de substances malodorantes. Ils ne s’utilisent qu’une dizaine de jours pour ne pas déséquilibrer la flore bucco-dentaire. Pensez aussi à brosser votre langue. Le brossage de la langue se fait d’arrière en avant pour éliminer les dépôts qui la recouvrent à l’aide d’un gratte-langue ou de la surface siliconée de la brosse à dents. N’utilisez jamais les poils de la brosse car ils risqueraient de créer des micros lésions sur la langue où pourraient se glisser les bactéries. </p>
