        <h3>2/12</h3>
        <h2>Une Gingivite ou une parodontite :</h2>
        <p>La gingivite est une inflammation de la gencive due à la présence de bactéries, qui s'infiltrent dans l'os au niveau de la racine de la dent et la détruisent. Si elle n’est pas traitée, elle peut se transformer en parodontite, les gencives sont sévèrement atteintes et les dents peuvent se déchausser. Ces bactéries stagnantes et non éliminées sont à l’origine des mauvaises odeurs buccales. La grossesse, le tabac, la ménopause peuvent provoquer l’apparition d’une gingivite. </p>
        <h2>Quel traitement ?</h2>
        <p>Une bonne hygiène bucco-dentaire permet de réduire et de prévenir l’évolution de la gingivite. Rendez-vous tout d’abord chez le dentiste pour poser le diagnostic de la gingivite et vérifier son degré d’évolution en mesurant la profondeur du sillon gingivo-dentaire, l’espace entre la gencive et la dent. Le spécialiste en profitera pour effectuer un détartrage (à effectuer tous les 6 mois à un an). Enfin pensez à vous laver les dents 2 à 3 fois par jour durant au moins 2 minutes comme le recommande l’UFSBD (l’union Française de la santé Bucco-dentaire). Vous pouvez y ajouter un peu de poudre de bicarbonate de sodium qui a le pouvoir de réduire les mauvaises odeurs. Sans oublier de passer un fil dentaire entre les dents afin d’éliminer les résidus alimentaires qui peuvent aussi être à l’origine de mauvaises odeurs s’ils stagnent trop longtemps dans la bouche. Les acides aminés venant des protéines des aliments donnent effectivement une odeur soufrée à l’haleine.</p>
