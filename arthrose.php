<h1 class="h1">L'arthrose:</h1>

<p class="p1">L'arthrose est une dégénérescence du cartilage des articulations sans infection ni inflammation particulière. Cependant, il existe des formes avec une composante inflammatoire et beaucoup de patients présentent des poussées d'inflammation sporadiques. Cette dégénérescence conduit à une destruction plus ou moins rapide du cartilage qui enrobe l'extrémité des os. Anatomiquement, cette destruction s'accompagne d'une prolifération osseuse sous le cartilage.<br>

C'est la maladie articulaire la plus fréquente. Les premiers symptômes apparaissent généralement à partir de 40-50 ans, mais la maladie commence souvent bien plus tôt dans la vie.</p><br>

<h1 class="h1">Causes et facteurs de risque de l'arthrose:</h1>

<p class="p1">Dans l'arthrose, la destruction du cartilage correspond à une fissuration de la surface vers la profondeur du tissu cartilagineux. Cette fissuration est liée à des phénomènes mécaniques, mais elle est également favorisée par des altérations biochimiques de la structure du cartilage.<br>

Schématiquement, on peut considérer que l'arthrose est le résultat :<br>
	
    &emsp;&emsp;&emsp;-De contraintes physiques anormales sur un cartilage normal ;<br>
    &emsp;&emsp;&emsp;-De contraintes physiques normales sur un cartilage anormal ;<br>
    &emsp;&emsp;&emsp;-De la conjonction des deux situations précédentes.<br>
Les principaux facteurs de risques suspectés sont :<br>


   &emsp;&emsp; - Généraux : âge, poids, ménopause, autres rhumatismes (chondrocalcinose, hyperostose de Forestier…) ;<br>
   &emsp;&emsp; - Génétiques : la notion d'arthrose familiale est bien démontrée pour les arthroses du genou, de la hanche et de la main ;<br>
   &emsp;&emsp; - Locaux :<br>
   &emsp;&emsp;&emsp;&emsp;&emsp; -Traumatismes importants ou faibles à répétition (travaux pénibles, sports violents, lésion du ménisque…) ;<br>
   &emsp;&emsp;&emsp;&emsp;&emsp; -Anomalie de position de l'articulation (scoliose, malformation de la hanche, …) ;<br>
   &emsp;&emsp;&emsp;&emsp;&emsp; -Autres maladies osseuses ou articulaires localisées (séquelles d'arthrite, séquelles de fractures, maladie de Paget…).<br>

A l'origine de l'arthrose interviennent de nombreux facteurs souvent encore mal connus. Cependant, le caractère génétique de l'affection semble prédominant. Il existe des familles d'arthrosiques et la maladie atteint plus fréquemment les femmes que les hommes. Par exemple, si certaines professions (travaux de force) sont plus exposées à l'arthrose que d'autres, tous les membres de cette profession ne feront pas d'arthrose (en partie en fonction de leur prédisposition génétique).<br>

Le dépistage des anomalies articulaires congénitales, suivi d'une correction dans l'enfance, est d'une importance capitale pour éviter l'apparition d'une arthrose douloureuse à l'âge adulte.<br>

L'obésité (ou le simple surpoids) est sans aucun doute un facteur favorisant l'apparition de l'arthrose au niveau des hanches et surtout des genoux. <br>

Pour en savoir plus, découvrez notre article sur les facteurs de risque de l'arthrose.
</p>

<h1 class="h1">Les symptômes de l'arthrose:</h1>

<p class="p1">Les signes de la maladie arthrosique varient selon l'articulation concernée. Cependant, dans tous les cas, le motif principal de consultation est la douleur associée à une gêne fonctionnelle .

La douleur est, en principe, dite de type "mécanique" car elle présente les caractéristiques suivantes :<br>

   &emsp;&emsp;&emsp;- Elle est déclenchée et aggravée par le mouvement ;<br>
   &emsp;&emsp;&emsp;- Elle cesse ou s'atténue plus ou moins complètement lorsque l'articulation est au repos ;<br>
   &emsp;&emsp;&emsp;- Elle est moins importante le matin, puis elle augmente dans la journée et elle est maximale le soir ;<br>
   &emsp;&emsp;&emsp;- Elle gêne traditionnellement l'endormissement, mais peut également entraîner des réveils nocturnes (environ 50 % des patients arthrosiques sont réveillés la nuit par leur douleur) ;<br>
   &emsp;&emsp;&emsp;- Elle réapparaît chaque fois que l'articulation concernée est soumise à un effort : la marche pour l'arthrose de hanche, monter un escalier pour le genou, lever le bras pour l'épaule…<br>

<b>La gêne fonctionnelle</b> correspond à une limitation de la mobilité de l'articulation touchée par l'arthrose. Elle est variable selon l'activité du patient. Ainsi, un joueur de golf sera beaucoup plus gêné par une arthrose du genou qu'un sujet ne pratiquant pas de sport. De même qu'un pianiste sera très handicapé par une arthrose des doigts, même légère.<br>

Les articulations arthrosiques ne sont, en principe, ni rouges, ni chaudes. Elles peuvent être gonflées lorsque s'installe un épanchement liquidien (épanchement de synovie), ce qui est particulièrement fréquent au niveau des genoux. Cependant des périodes de poussée inflammatoire existent et il y aurait une composante inflammatoire dans cette maladie, différente et moins importante que celle de la polyarthrite rhumatoïde.<br>

A la longue, les excroissances osseuses ostéophytes provoquent des déformations des articulations, surtout visibles au niveau des mains et des genoux.<br>

L'état général du patient est toujours bon. Il n'y a ni fièvre ni amaigrissement.<br>

Les lésions arthrosiques sont irréversibles et aboutissent, outre les déformations, à un raidissement articulaire pouvant évoluer vers une impotence partielle.

</p>



