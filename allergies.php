<h1 class="h1">Les symptômes des différentes formes d’allergies</h1>

<p class="p1">L’OMS estime que 50 % de la population mondiale sera affectée par des allergies, toutes formes confondues, d’ici 2050. Les signes et symptômes d’une réaction allergique sont nombreux et peuvent aller de simples écoulements nasaux à des chocs anaphylactiques potentiellement fatals. Comment les reconnaître ? Quels sont ces symptômes des allergies alimentaires, cutanées et respiratoires et comment réagir ? Les réponses avec le Dr Dalila Nouar, praticien hospitalier dans le service d’Allergologie et Immunologie clinique du CHRU de Tours.</p><br>

<p class="p1">Les modifications de notre environnement et de nos modes de vie ont un impact important sur les maladies allergiques. "En France, les allergies sont en forte augmentation également", explique le Dr Dalila Nouar. "Notre mode de vie, toujours plus aseptisé, est également un des facteurs de développement des maladies allergiques. A force, notre corps ne sait plus bien distinguer ce qui est allergisant ou non." Résultat : de plus en plus d’allergies, aussi bien alimentaires que cutanées ou respiratoires.</p><br>

<h1 class="h1">Symptômes de l’allergie alimentaire:</h1>

<p class="p1">L’allergie alimentaire se manifeste par une réaction immédiate ou dans un délai d’1 à 2 heures, après ingestion de l’aliment en cause. "Elle peut être très grave voire fatale", prévient le Dr Nouar.</p><br>

<p class="p1">Concernant les symptômes, ils peuvent être modérés comme très alarmants, avec un risque vital :<br>

  	•	Urticaire ;<br>
	•	Œdème de la face ;<br>
	•	Prurit buccopharyngé ;<br>
	•	Gêne respiratoire ;<br>
	•	Douleurs abdominales, diarrhées ;<br>
	•	Perte de connaissance ;<br>
	•	Arrêt cardiaque, dans les cas extrêmes.<br>


En cas de suspicion d’allergie à certains aliments, il est absolument nécessaire de consulter un allergologue.</p><br>
<p class="p1">"Beaucoup de ces aliments engendrent en fait des réactions d’hypersensibilité non allergique", nuance le Dr Nouar, "Ce qui fait que beaucoup de personnes se croient allergiques alors qu’elles ne le sont pas réellement. Il faut donc les différencier des vraies réactions allergiques alimentaires."<br>

Pour ce faire, l’allergologue réalisera des tests cutanés aux aliments ainsi que des prises de sang spécifiques. Dans une structure d’hôpital de jour, l’expert pourra même aller jusqu’au test de réintroduction de l’aliment incriminé.<br>

En cas d’allergie alimentaire prouvée, cet aliment sera définitivement contre-indiqué et le patient devra toujours disposer d’une trousse d’urgence avec un stylo d’adrénaline en cas d’ingestion par inadvertance de cet allergène ou en cas d’allergène caché.</p><br>

<h1 class="h1">Symptômes de l’allergie cutanée</h1>

<p class="p1">Il existe différentes formes d’allergies cutanées, dont les symptômes peuvent varier en fonction du délai d’apparition.<br>
	•	L’urticaire : elle peut être liée à une allergie alimentaire ou médicamenteuse ou aux venins d’hyménoptères. "Dans ce cas présent, il faut être vigilant 	 à ce qu’il n’y ait pas une aggravation des symptômes avec un œdème de la face ou une anaphylaxie sévère", avertit l’experte.<br>
	•	Des eczémas : ils se manifestent par un "érythème avec des plaques sèches ou par des démangeaisons".<br>
	•	Des toxidermies érythémateuses : "C’est le nom d’un ensemble de lésions cutanées, retardées, conséquentes d’un médicament, ou d’un allergène appliqué sur la peau", affirme le Dr Nouar.
</p><br>

<p class="p1">Pour ces cas de figure, il convient d’aller consulter un allergologue pour identifier l’allergène. "Il faut trouver le facteur déclenchant : il s’agit d’un interrogatoire très précis. Ce peuvent être des produits cosmétiques, chimiques, des métaux, des médicaments etc.", énumère l’allergologue. Lors de la consultation, le professionnel de santé qualifié utilisera un patch test pour déterminer le médicament incriminé et établir s’il y a allergie de contact ou non.</p><br>

<h1 class="h1">Symptômes de l’allergie respiratoire</h1>

<p class="p1">Plusieurs symptômes traduisent une allergie respiratoire. La principale reste la gêne respiratoire, qui se traduit par des sifflements ou une oppression de la cage thoracique, ou une toux sèche.<br>

Dans un contexte de sensibilisation aux aéroallergènes (acariens, pollens, phanères d’animaux, moisissures), peuvent survenir :<br>
	•	Une rhinite : une inflammation des muqueuses nasales ;<br>
	•	Une conjonctivite ; une inflammation de la conjonctive de l’œil qui devient rouge ;<br>
	•	Un asthme ; une inflammation des voies respiratoires.<br>

</p><br>
<p class="p1">En cas de symptômes de ce type, la consultation chez l’allergologue servira à déceler la source de l’allergie grâce à des tests cutanés. "Ces derniers déterminent le facteur déclenchant grâce à la saison et établissent si c’est l’allergie est plutôt saisonnière ou perannuelle", raconte le Dr Nouar.<br>

Une désensibilisation de l’agent allergène (fréquemment des acariens) peut alors être envisagée "Pour guérir totalement ces symptômes, seul un traitement de désensibilisation sera efficace."</p><br>


