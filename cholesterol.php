<h1 class="h1">Qu'est-ce que le cholestérol et à quoi sert-il ?</h1>

<p class="p1">Le cholestérol est une graisse naturelle indispensable à l'organisme. Il permet entre autres la synthèse de la vitamine D ou de la bile. C'est un constituant de la paroi de nos cellules. Enfin, il entre dans la composition de nombreuses hormones, comme les hormones sexuelles.</p><br>

<h1 class="h1">D'où vient le cholestérol ?</h1>

<p class="p1">Il est pour l'essentiel produit par le foie (3/4) et le reste provient de l'alimentation. On le trouve dans les produits d'origine animale à des taux très variables : viandes, abats, produits laitiers, crustacés, coquillages, poissons, oeufs...</p><br>

<h1 class="h1">Comment s'équilibre le taux de cholestérol ?</h1>

<p class="p1">Le LDL Cholestérol transporte le cholestérol provenant des aliments vers les tissus. Les HDL Cholestérol a pour fonction de transporter l'excédent de cholestérol dans le sang vers le foie et participe ainsi à l'élimination de cette graisse par l'organisme. Mais un dessin vaut mieux qu'un long discours.</p><br>

<h1 class="h1">Comment savoir si on a trop de cholestérol ?</h1>

<p class="p1">Le cholestérol circule librement dans le sang, ainsi il peut être aisément dosé grâce à une simple prise de sang. On pourra doser dans un premier temps le dosage du cholestérol total (bon cholestérol + mauvais cholestérol). Si ce bilan se révèle perturbé (supérieur à 2g/l et triglycérides supérieur à 2 g/l), le médecin demandera un bilan plus approfondi avec dosage du mauvais cholestérol et du bon cholestérol.<br>

En cas de risque cardiovasculaires associés, le médecin pourra procéder d'emblée à un dosage approfondi. Depuis peu, le dosage du cholestérol total peut se faire à domicile grâce à un test individuel vendu en pharmacie.</p><br>

