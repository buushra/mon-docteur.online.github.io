                <h2 class="text-uppercase">Alzheimer </h2>
                <p class="item-intro text-muted">10 signes précurseurs de la maladie</p>
                <img class="img-fluid d-block mx-auto" src="image/alzheimer.png" alt="">
                <p>Des pertes de mémoire, des troubles du raisonnement, des changements d'humeur et de comportement... Il existe de nombreux signaux précurseurs de la maladie d'Alzheimer qui peuvent alerter le patient et son entourage. Inutile pour autant de s'alarmer car il existe des symptômes similaires causés par d'autres maladies pour lesquelles existent des traitements médicaux. C'est pourquoi, si plusieurs de ces symptômes coexistent chez une même personne, il est important de consulter un médecin afin de confirmer le diagnostic de la maladie d'Alzheimer.</p>
                <p><b>1 - Pertes de mémoire:</b><br>
                Oublier occasionnellement un rendez-vous, le nom d'un collègue ou un numéro de téléphone et s'en rappeler plus tard est un phénomène normal. En revanche une personne atteinte de la maladie d'Alzheimer oubliera fréquemment des événements récents et importants pour ne jamais s'en souvenir.</p>
                <p><b>2 - Difficultés à exécuter les tâches familières:</b><br>
                Dans le cadre de nos activités quotidiennes, il nous arrive à tous d'être distraits et de mal nous y prendre. Une personne atteinte de la maladie d'Alzheimer peut ne plus être capable d'écrire, de cuisiner, d'éplucher des légumes, de boutonner sa veste, etc.</p>
                <p><b>3 - Problèmes de langage:</b><br>
                Il peut être parfois difficile de trouver le mot juste. Une personne atteinte de la maladie d'Alzheimer perdra la capacité de finir ses phrases ou y substituera des mots inappropriés. Il est alors difficile pour les autres de comprendre ce qu'elle dit.</p>
                <p><b>4 - Désorientation dans l'espace et dans le temps:</b><br>
                Il peut arriver que l'on oublie temporairement le jour de la semaine ou même l'endroit où l'on va. Mais une personne atteinte de la maladie d'Alzheimer peut se croire à une autre saison, une autre année déjà éloignée ou se perdre dans un endroit connu : elle ne sait plus comment elle s'est rendue là, ni comment rentrer chez elle.</p>
                <p><b>5 - Jugement affaibli:</b><br>
                Parfois, lorsqu'on ne se sent pas bien, on tarde à se faire soigner. Mais avec la persistance ou l'aggravation des signes on finit par se rendre chez le médecin. Une personne atteinte de la maladie d'Alzheimer ne s'alarme pas de la gravité de ses difficultés et les banalise même, ce qui inquiète son entourage. Par ailleurs, une personne atteinte de la maladie d'Alzheimer n'est plus capable de juger une situation et pourra prendre des décisions déraisonnables.</p>
                <p><b>6 - Difficultés face aux notions abstraites:</b><br>
                En vieillissant, une personne peut éprouver de la difficulté à s'adapter à de nouvelles techniques. Une personne atteinte de la maladie d'Alzheimer peut ne plus comprendre ce que représentent les chiffres et ce qu'elle doit faire. Nombreux sont ceux qui vont faire des dépenses inconsidérées qu'ils n'auraient jamais faites auparavant.</p>
                <p><b>7 - Objets égarés:</b><br>
                Quiconque peut égarer temporairement son porte-monnaie ou ses clés. Une personne atteinte de la maladie d'Alzheimer rangera les objets dans des endroits inappropriés (un fer à repasser dans le congélateur ou une montre dans le sucrier par exemple).</p>
                <p><b>8 - Changements d'humeur ou de comportement:</b><br>
                Il nous arrive à tous d'être triste et maussade. Une personne atteinte de la maladie d'Alzheimer peut changer d'humeur très rapidement (du calme aux pleurs ou à la colère) sans raison apparente.</p>
                <p><b>9 - Changements de personnalité:</b><br>
                La personnalité de chacun peut changer quelque peu avec l'âge. Chez une personne atteinte de la maladie d'Alzheimer, on observera des changements très prononcés. Par exemple, une personne autrefois ouverte et confiante deviendra renfermée et méfiante. Au nombre des changements possibles, on compte aussi l'apathie, la peur et les comportements difficiles.</p>
                <p><b>10 - Manque d'enthousiasme:</b><br>
                Il nous arrive à tous, à l'occasion, de nous lasser de l'entretien ménager, de notre travail ou de nos activités sociales, mais la plupart des gens retrouvent vite leur enthousiasme. Une personne atteinte de la maladie d'Alzheimer deviendra passive plus rapidement sans pour autant avoir un discours dépressif. Elle se désintéressera même d’événements importants.</p>

